namespace Ion.ErrorReporting
{
    public static class InternalErrorNames
    {
        /// <summary>
        /// A general error.
        /// </summary>
        public const string Generic = "Generic";

        /// <summary>
        /// Represents syntax errors caused by improper
        /// token positions or invalid syntax.
        /// </summary>
        public const string Syntax = "Syntax";

        /// <summary>
        /// Represents syntax errors caused by an argument
        /// mismatch.
        /// </summary>
        public const string ArgumentMismatch = "ArgumentMismatch";
    }
}
